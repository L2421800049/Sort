void show(int *arr,int length){
    for(int i=0;i<length;i++){
        printf("%d ",arr[i]);
    }
    printf("\n\n");
}

void countSort(elemType *arr){
    elemType temp[MAXSIZE];
    // 中间数组,计数值全部置为0，范围是RANGE
    for(int i=0;i<RANGE;i++) temp[i] = 0;
    //遍历原始数组
    for (int i=0;i<MAXSIZE;i++){
        // 计数数组响应的值加1
        temp[arr[i]]++;
    }
    show(temp,RANGE);
    for(int i=0,j=0;i<RANGE;i++){
        while (temp[i]>0)
        {
            arr[j++]=i;
            temp[i]--;
        }
    }
}


void bubble(int *p,int length){

    int i,temp,count=0;
    while (1)
    {
        for(i=0;i<length-1;i++){
            if(p[i]>p[i+1]) {
                temp = p[i];p[i] = p[i+1];p[i+1] = temp;count++;
            }
        }
        if (count == 0) break;
        count = 0;
    }
}


